import org.json.JSONObject

public class JsonExtractor {

	/*
	 * Extracts title and author from a JSON string.
	 *
	 * @param jsonString The JSON string containing title and author.
	 * @return A map containing the extracted title and author.
	 */
	@Keyword
	def extractTitleAndAuthor(String jsonString) {
		JSONObject json = new JSONObject(jsonString)
		String title = json.getString("title")
		String author = json.getString("author")

		return [title: title, author: author]
	}
}
